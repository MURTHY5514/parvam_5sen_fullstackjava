package com.datatypes;

public class PrimitiveTypes {
	//helloWorld -> variables or functions
	//HelloWorld -> class or interface
	// single line comment
	/*
	 * multi-line
	 * comment
	 * */
	//Primitive Data Types
	public static void main(String[] args) {
		//variables
		//int
		int age = 25;
		System.out.println("Your age is : "+age);
		// byte, short ,long, float,double, char, boolean
		
		byte stdNo =12;
		System.out.println("Student no: "+stdNo);
		
		short s=1000;
		System.out.println("s value: "+s);
		
		long phoNo = 998097L;
		System.out.println("phoneNo is: "+phoNo);
		
		float marks = 123.89f;
		System.out.println("Marks is: "+marks);
		
		boolean isActive = true;
		System.out.println("user is: "+isActive);
		
		double marks1 = 134.56;
		System.out.println("Double: "+marks1);
		
		char gender = 'M';
		System.out.println("Gender: "+gender);
	}
}
